   public void detenerMusica() {
    try {
        if (Inicio.pidMusica != null) {
            new ProcessBuilder(
                    "flatpak-spawn", "--host",
                    "kill", "-9", String.valueOf(Inicio.pidMusica)
            ).start().waitFor();

            Inicio.pidMusica = null;
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}
    /**
     * Reproduce música a partir de un archivo de sonido especificado. Configura
     * el bucle antes de iniciar el clip de sonido.
     *
     * @param archivo Nombre del archivo de sonido sin la extensión (.wav).
     */
   public void music(String archivo) {
    try {
        detenerMusica();

        String rutaArchivo = ruta + archivo + ".wav";

        Process p = new ProcessBuilder(
                "flatpak-spawn", "--host",
                "bash", "-lc",
                "paplay \"" + rutaArchivo + "\" >/dev/null 2>&1 & echo $!"
        ).start();

        java.io.BufferedReader reader = new java.io.BufferedReader(
                new java.io.InputStreamReader(p.getInputStream())
        );

        String pidTexto = reader.readLine();

        if (pidTexto != null && !pidTexto.isBlank()) {
            Inicio.pidMusica = Long.parseLong(pidTexto.trim());
        }

    } catch (Exception e1) {
        try {
            detenerMusica();

            String rutaArchivo = ruta + archivo + ".wav";

            Process p = new ProcessBuilder(
                    "flatpak-spawn", "--host",
                    "bash", "-lc",
                    "aplay -q \"" + rutaArchivo + "\" >/dev/null 2>&1 & echo $!"
            ).start();

            java.io.BufferedReader reader = new java.io.BufferedReader(
                    new java.io.InputStreamReader(p.getInputStream())
            );

            String pidTexto = reader.readLine();

            if (pidTexto != null && !pidTexto.isBlank()) {
                Inicio.pidMusica = Long.parseLong(pidTexto.trim());
            }

        } catch (Exception e2) {
            System.out.println("No se pudo reproducir la música: " + archivo);
            e2.printStackTrace();
        }
    }
}

    /**
     * Reproduce un sonido a partir de un archivo de sonido especificado.
     *
     * @param archivo Nombre del archivo de sonido sin la extensión (.wav).
     */
    public void sonido(String archivo) {
    try {
        String rutaArchivo = ruta + archivo + ".wav";

        new ProcessBuilder(
                "flatpak-spawn", "--host",
                "paplay", rutaArchivo
        ).start();

    } catch (Exception e1) {
        try {
            String rutaArchivo = ruta + archivo + ".wav";

            new ProcessBuilder(
                    "flatpak-spawn", "--host",
                    "aplay", "-q", rutaArchivo
            ).start();

        } catch (Exception e2) {
            System.out.println("No se pudo reproducir el sonido: " + archivo);
            e2.printStackTrace();
        }
    }
}


