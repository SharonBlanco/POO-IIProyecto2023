    /**
     * Maneja el evento de clic en el botón de empezar juego. Detiene la música
     * de fondo, oculta la ventana actual y crea una nueva instancia de la clase
     * Juegos para comenzar el juego.
     *
     * @param evt Evento generado por el botón empezar.
     */
    private void empezarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empezarActionPerformed
        if (paises.isSelected() || cine.isSelected() || culturaGeneral.isSelected() || sorpresa.isSelected()) {

            detenerMusica();
            setVisible(false);
            Juegos juego;
            if (PreguntasPorCategoria == null) {
                try {
                    PreguntasPorCategoria = Juegos.ElegirPreguntas(this.lista);
                } catch (excepcionPreguntasNoDisponibles ex) {
                    Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            try {
                juego = new Juegos();
                juego.setVisible(true);
            } catch (excepcionPreguntasNoDisponibles ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_empezarActionPerformed
